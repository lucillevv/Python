getal1 = int(input("geef het eerste getal: "))
getal2 = int(input("geef het tweede getal: "))
getal3 = int(input("geef het derde getal: "))

gemiddelde = (getal1 + getal2 + getal3) / 3
print(max(getal1, getal2, getal3))
print(min(getal1, getal2, getal3))
print("het gemiddelde is {:.2f}".format(gemiddelde))
